function pushbutton4_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton4 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
I6 = handles.ImgData2;
I = I6;
%% Extract Features

% Function call to evaluate features
%[feat_disease seg_img] =  EvaluateFeatures(I)

% Color Image Segmentation
% Use of K Means clustering for segmentation
% Convert Image from RGB Color Space to L*a*b* Color Space 
% The L*a*b* space consists of a luminosity layer 'L*', chromaticity-layer 'a*' and 'b*'.
% All of the color information is in the 'a*' and 'b*' layers.
cform = makecform('srgb2lab');
% Apply the colorform
lab_he = applycform(I,cform);

% Classify the colors in a*b* colorspace using K means clustering.
% Since the image has 3 colors create 3 clusters.
% Measure the distance using Euclidean Distance Metric.
ab = double(lab_he(:,:,2:3));
nrows = size(ab,1);
ncols = size(ab,2);
ab = reshape(ab,nrows*ncols,2);
nColors = 3;
[cluster_idx cluster_center] = kmeans(ab,nColors,'distance','sqEuclidean', ...
                                      'Replicates',3);
%[cluster_idx cluster_center] = kmeans(ab,nColors,'distance','sqEuclidean','Replicates',3);
% Label every pixel in tha image using results from K means
pixel_labels = reshape(cluster_idx,nrows,ncols);
%figure,imshow(pixel_labels,[]), title('Image Labeled by Cluster Index');

% Create a blank cell array to store the results of clustering
segmented_images = cell(1,3);
% Create RGB label using pixel_labels
rgb_label = repmat(pixel_labels,[1,1,3]);

for k = 1:nColors
    colors = I;
    colors(rgb_label ~= k) = 0;
    segmented_images{k} = colors;
end



figure,subplot(2,3,2);imshow(I);title('Orjinal Görüntü'); subplot(2,3,4);imshow(segmented_images{1});title('Segmente Edilmiþ 1.Görüntü'); subplot(2,3,5);imshow(segmented_images{2});title('Segmente Edilmiþ 2.Görüntü');
subplot(2,3,6);imshow(segmented_images{3});title('Segmente Edilmiþ 3.Görüntü');
set(gcf, 'Position', get(0,'Screensize'));
set(gcf, 'name','Segmented by K Means', 'numbertitle','off')
% Feature Extraction
pause(2)
x = inputdlg('Enfekte olan bölgenin en net göründüðü görüntünün numarasýný lütfen giriniz:');
i = str2double(x);
% Extract the features from the segmented image
seg_img = segmented_images{i};

% Convert to grayscale if image is RGB
if ndims(seg_img) == 3
   img = rgb2gray(seg_img);
end
%figure, imshow(img); title('Gray Scale Image');

% Evaluate the disease affected area
black = im2bw(seg_img,graythresh(seg_img));
%figure, imshow(black);title('Black & White Image');
m = size(seg_img,1);
n = size(seg_img,2);

zero_image = zeros(m,n); 
%G = imoverlay(zero_image,seg_img,[1 0 0]);

cc = bwconncomp(seg_img,6);
enfektebolge = regionprops(cc,'basic');
A1 = enfektebolge.Area;
sprintf('Enfekte olan alan : %g%',A1);

I_black = im2bw(I,graythresh(I));
kk = bwconncomp(I,6);
totalalan = regionprops(kk,'basic');
A2 = totalalan.Area;
sprintf(' total alan : %g%',A2);

%Affected_Area = 1-(A1/A2);
Enfekte_Bolge = (A1/A2);
if Enfekte_Bolge < 0.1
    Enfekte_Bolge = Enfekte_Bolge*100;
    
end
sprintf('Enfekte olan alan: %g%%',(Enfekte_Bolge*100))
enfekte = Enfekte_Bolge*100;
% Create the Gray Level Cooccurance Matrices (GLCMs)
glcms = graycomatrix(img);

% Derive Statistics from GLCM
stats = graycoprops(glcms,'Contrast Correlation Energy Homogeneity');
Kontrast = stats.Contrast;
Korelasyon = stats.Correlation;
Enerji = stats.Energy;
Homojenlik = stats.Homogeneity;
Ortalama = mean2(seg_img);
Standart_Sapma= std2(seg_img);
Entropi = entropy(seg_img);
Karekok_Ortalamasi = mean2(rms(seg_img));
%Skewness = skewness(img)
Varyans = mean2(var(double(seg_img)));
a = sum(double(seg_img(:)));
Puruzsuzluk = 1-(1/(1+a));
Basiklik = kurtosis(double(seg_img(:)));
carpiklik = skewness(double(seg_img(:)));
% Inverse Difference Movement
m = size(seg_img,1);
n = size(seg_img,2);
in_diff = 0;
for i = 1:m
    for j = 1:n
        temp = seg_img(i,j)./(1+(i-j).^2);
        in_diff = in_diff+temp;
    end
end
IDM = double(in_diff);
    
degerler = [Kontrast,Korelasyon,Enerji,Homojenlik,Ortalama, Standart_Sapma, Entropi, Karekok_Ortalamasi, Varyans, Puruzsuzluk, Basiklik, carpiklik, IDM];
I7 = imresize(seg_img,[300,400]);
axes(handles.axes3);
imshow(I7);title('Segmente Edilmiþ Görüntü');
set(handles.edit3,'string',enfekte);
set(handles.edit5,'string',Ortalama);
set(handles.edit6,'string',Standart_Sapma);
set(handles.edit7,'string',Entropi);
set(handles.edit8,'string',Karekok_Ortalamasi);
set(handles.edit9,'string',Varyans);
set(handles.edit10,'string',Puruzsuzluk);
set(handles.edit11,'string',Basiklik);
set(handles.edit12,'string',carpiklik);
set(handles.edit14,'string',Kontrast);
set(handles.edit15,'string',Korelasyon);
set(handles.edit16,'string',Enerji);
set(handles.edit17,'string',Homojenlik);
handles.ImgData3 = degerler;
handles.ImgData4 = enfekte;
% Update GUI
guidata(hObject,handles);

